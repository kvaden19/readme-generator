// Function that returns a license badge based on which license is passed in
// If there is no license, return an empty string
function renderLicenseBadge(license) {
  let badgeString = ``;
  if (!license) {
    return badgeString
  }

  badgeString = `![license](https://img.shields.io/badge/license-${license}-green)`;
  return badgeString;
}

// Function that returns the license link
// If there is no license, return an empty string
function renderLicenseLink(license) {
  let link = ``;
  if (!license) {
    return link
  }

  // Switch statement
  switch(license) {
    case 'Apache 2.0':
      link = 'http://www.apache.org/licenses/LICENSE-2.0';
      break;
    case 'Boost Software 1.0':
      link = 'http://www.boost.org/LICENSE_1_0.txt';
      break;
    case 'GNU AGPLv3':
      link = 'https://www.gnu.org/licenses/agpl.txt';
      break;
    case 'GNU GPLv3':
      link = 'https://www.gnu.org/licenses/gpl-3.0-standalone.html';
      break;
    case 'GNU LGPLv3':
      link = 'https://www.gnu.org/licenses/lgpl-3.0-standalone.html';
      break;
    case 'MIT':
      link = 'https://opensource.org/licenses/MITnse';
      break;
    case 'Mozilla Public 2.0':
      link = 'http://www.mozilla.org/MPL/2.0/';
      break;
    case 'Unilicense':
      link = 'https://unlicense.org/';
      break;
  }
  linkMarkdown = `[${link}](${link})<br>`;
  return linkMarkdown;
}

